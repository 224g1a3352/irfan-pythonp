import turtle
turtle.setup(800,600)
window=turtle.Screen()
window.title("My first gf")
t=turtle.Turtle()
t.resizemode('user')
t.turtlesize(3,3)
turtle.colormode(255)
t.pensize(5)
t.pencolor(238,130,238)
t.penup()
t.goto(-150,0)
t.left(60)
t.forward(100)
t.pendown()
t.right(120)
t.forward(100)
t.left(120)
t.forward(100)
t.right(120)
t.forward(100)
t.left(120)
t.forward(100)
turtle.exitonclick()

